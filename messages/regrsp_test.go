package messages

import "testing"

var mockRegRsp = []byte{0x1, 0x0, 0x9, 0x2, 0x0, 0x0, 0x0, 0x24, 0x2, 0x8, 0x0, 0x1c, 0x2, 0xa, 0x0, 0x8, 0x0, 0x0, 0x0, 0x1, 0x2, 0x12, 0x0, 0x8, 0x0, 0x0, 0x0, 0x0, 0x0, 0x6, 0x0, 0x8, 0x0, 0x0, 0x0, 0x6}

func TestRegRsp(t *testing.T) {
	r, err := ParseRegRsp(mockRegRsp)
	if err != nil {
		t.Fatal(err)
	}
	rc, err := r.FetchRC()
	if (rc != 6) || err != nil {
		t.Fatal(err)
	}
}
